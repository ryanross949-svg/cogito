const fs = require('fs');

// course id -> {name, domain} from the hub APPS (for display + discipline)
const hub = fs.readFileSync('index.html', 'utf8');
const meta = {};
const re = /id: '([a-z]+)', name: '([^']*)', domain: '([a-z]+)'/g;
let m;
while ((m = re.exec(hub)) !== null) meta[m[1]] = { name: m[2], domain: m[3] };

const NON_COURSE = new Set(['index.html', 'review.html']);
const files = fs.readdirSync('.').filter(f => f.endsWith('.html') && !NON_COURSE.has(f)).sort();

const out = {};
const problems = [];

files.forEach(f => {
  const id = f.replace('.html', '');
  const text = fs.readFileSync(f, 'utf8');
  const grab = (name) => {
    const mm = text.match(new RegExp('const ' + name + '\\s*=\\s*(\\[[\\s\\S]*?\\n\\]);'));
    if (!mm) return null;
    try { return eval('(' + mm[1] + ')'); } catch (e) { return null; }
  };
  const km = text.match(/const STORAGE_KEY\s*=\s*'([^']+)'/);
  const MODULES = grab('MODULES');
  const CARDS = grab('CARDS');
  if (!km || !MODULES || !CARDS) { problems.push(f + ' (key=' + !!km + ' mods=' + !!MODULES + ' cards=' + !!CARDS + ')'); return; }
  const ARENA = grab('ARENA') || [];
  const PHASES = grab('PROJECT_PHASES') || [];

  const modules = MODULES.map(mod => {
    const cards = CARDS.filter(c => c.moduleId === mod.id);
    return {
      id: mod.id,
      codename: mod.codename || mod.id,
      title: mod.title || '',
      cards: cards.map(c => c.id),
      coreCards: cards.filter(c => c.core).map(c => c.id),
      arena: ARENA.filter(p => p.moduleId === mod.id).map(p => p.id),
      phase: (PHASES.find(p => p.moduleId === mod.id) || {}).id || null
    };
  });

  out[id] = {
    key: km[1],
    name: (meta[id] && meta[id].name) || id.toUpperCase(),
    domain: (meta[id] && meta[id].domain) || 'unknown',
    href: f,
    moduleCount: MODULES.length,
    cardCount: CARDS.length,
    arenaCount: ARENA.length,
    phaseCount: PHASES.length,
    modules: modules
  };
});

if (problems.length) { console.error('PROBLEMS:\n  ' + problems.join('\n  ')); process.exit(1); }

const head = '/* AUTO-GENERATED by scripts/gen-structure.js. Maps each course into modules and the\n   card / arena / project-phase ids they own, so the hub can recommend module-level tasks. */\n';
fs.writeFileSync('course-structure.js', head + 'window.COURSE_STRUCTURE = ' + JSON.stringify(out) + ';\n');

const ids = Object.keys(out);
console.log('courses:', ids.length);
console.log('total modules:', ids.reduce((s, k) => s + out[k].modules.length, 0));
console.log('course-structure.js:', (fs.statSync('course-structure.js').size / 1024).toFixed(0) + ' KB');
